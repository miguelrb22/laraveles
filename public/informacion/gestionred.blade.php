    <h2><a href="#">Gestion de la red</a></h2>
</div>
<hr/>


<p>Desde multifranquicias gestionamos su red de franquicias en caso de acudir a nuestros profesionales con el único fin de rentabilizar su red de franquicias.</p>

<p>Para ello realizaremos labores de comunicación con todos los responsables de las franquicias centralizando un interés común. De este modo respetando la autonomía de cada franquiciado unificaremos la dirección deseada aprovechando la fuerza que genera una red de franquicias. Por lo tanto para que su franquicia sea rentable y exitosa gestionaremos la red del siguiente modo:</p>

<ul>
<li>Comunicación con los responsables
    <li>Valoración de lo manifestado por estos
    <li>Comparativo de situación análogas.
    <ul>
    <li>Situación previa
    <li>Medios humanos y logísticos
    <li>Objetivo futuro</ul>
    <li>Concreción de la dirección establecida para cumplir objetivos.

</ul>