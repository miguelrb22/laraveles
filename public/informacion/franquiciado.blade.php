
<div>
    <h2><a href="#">Franquicia: qué debemos saber</a></h2>
</div>
<hr/>
<p class="text-justify">Partiremos de la definición del art. 62 de la Ley 7/1996, de 15 de enero, de Ordenación del Comercio Minorista. Es franquiciado todo aquel al que se cede un mercado determinado, a cambio de una contraprestación financiera directa, indirecta o ambas, el derecho a explotación de una franquicia, sobre un negocio o actividad mercantil que el primero venga desarrollando anteriormente con suficiente experiencia y éxito.</p>
<p class="text-justify">Obligaciones más comunes de un franquiciado:</p>
<ul>
    <li>Abonar el total de la inversión para instalar y decorar el establecimiento donde se vaya a situar la franquicia.</li>
    <li>Realizar los pagos de cánones de entrada y periódicos pactados entre franquiciador y franquiciado. Estos pagos periódicos son contraprestaciones a los servicios continuados a cargo del franquiciador.</li>
    <li>No bajar del stock minimo establecido por el franquiciador</li>
    <li>Tener en cuenta lo precios del franquiciador</li>
    <li>Obligación de respeto hacia la imagen de la marca</li>
    <li>Llevar a cabo todos los controles que se hayan pactado en el contrato de franquicia</li>
    <li>Respetar los espacios físicos de otras franquicias.</li>
    <li>Deber de asistencia a las formaciones que se programen</li>
    <li>Realizar los mismos métodos de trabajo, método de comercialización y de gestión establecidos en los manuales de la franquicia.</li>
</ul>