    <h2><a href="#">Expansión internacional</a></h2>
</div>
<hr/>



<P>El modelo de franquicia es la idea de mayor extensión por antonomasia, todo franquiciador desea traspasar fronteras llegando a nuevos mercados. En multifranquicias contamos con especialistas y herramientas adecuadas para poder internacionalizar su franquicia.</P>

<P>
Para ello seguiremos un procedimiento:</P>

<UL>

<li>Estudio de la aceptación del mercado receptor
    <li>Análisis y determinación del modo de expansión de franquicia
    <li>Valorar objetivamente el potencial de la franquicia a internacionalizar
    <li>Previsión financiera para la franquicia
    <li>Preparación y redacción de documentos contractuales para la compra o adquisiciones de franquicias.
    <li>Localización de potenciales inversores en el mercado receptor.

    </UL>