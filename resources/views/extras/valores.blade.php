    <h2><a href="#">Valores de locales candidatos</a></h2>
</div>
<hr/>
<p class="text-justify">De acuerdo con las necesidades que se necesitan valoraremos los locales candidatos para su franquicia.</p>
<p class="text-justify">Para desarrollar tal valoración necesitaremos la documentación acreditativa indicando que necesitaría su local de franquicia. Y en segundo lugar, desarrollaremos un informe sobre las características de los locales.</p>
<p class="text-justify">En caso de no encajar ningún local de los propuestos por usted, desde MultiFranquicias <strong>buscaremos una solución a su problema presentando soluciones alternativas</strong> para que pueda desarrollar la actividad en tres regímenes diferentes:</p>
<ul>
    <li>Local en propiedad</li>
    <li>Local como arrendatario</li>
    <li>Local como usufructuario</li>
    <li>Local como mero precario</li>
</ul>
