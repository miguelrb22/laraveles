    <h2><a href="#">Tipos de franquicias</a></h2>
</div>
<hr/>
<p class="text-justify">Tipos de franquicias:</p>
<ol>
    <li>
        <strong>Franquicia comercial</strong>
        <ul>
            <li>Se produce cuando el franquiciador desde a sus franquiciados, todos los elementos necesarios para la venta de productos o servicios al consumidor final</li>
        </ul>
    </li>
    <li>
        <strong>Franquicia industrial</strong>
        <ul>
            <li>Cesión del derecho de fabricación, la infraestructura y el modo de comercialización, un ejemplo muy utilizado son las franquicias de comida</li>
        </ul>
    </li>
    <li>
        <strong>Franquicia de distribución o de producto</strong>
        <ul>
            <li>Es aquella que tiene como objeto la distribución de productos o servicios</li>
        </ul>
    </li>
    <li>
        <strong>Franquicia de servicio</strong>
        <ul>
            <li>Se identifica con aquellas franquicias cuyo lucro se realiza mediante la realización de servicios a consumidores.</li>
        </ul>
    </li>
    <li>
        <strong>Franquicia de córner</strong>
        <ul>
            <li>Parte de un espacio físico en el cual se define otro menor en él se desarrolla la franquicia</li>
        </ul>
    </li>
    <li>
        <strong>Shop in shop</strong>
        <ul>
            <li>Es una recreación de la decoración de otro establecimiento integrado dentro de la cadena. Además de la distinción anterior, las franquicias también se pueden dividir por mercado y estructura:</li>
        </ul>
    </li>
    <li>
        <strong>Franquicia individual</strong>
        <ul>
            <li> Contrato especifico a favor de una persona</li>
        </ul>
    </li>
    <li>
        <strong>Franquicia múltiple</strong>
        <ul>
            <li>Un franquiciado de un territorio podrá abrir otras franquicias en un determinado periodo de tiempo</li>
        </ul>
    </li>
    <li>
        <strong>Franquicia regional</strong>
        <ul>
            <li>Franquiciado en un área geográfico, que en caso de funcionar se modificará la limitación pudiendo abrir más franquicias.</li>
        </ul>
    </li>

</ol>